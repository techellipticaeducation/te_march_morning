package te_march_morning.operator;

public class ComparisonOperatorDemo {
	public static void main(String[] args) {
		// > - morethen
		// < - less then
		// >= //morethen equal
		// <= // less then equal to
		// == //  equal
		// !=  //  not equal
		
		// result = true/false boolean
		
		int i = 10;
		int j = 10;
		
		System.out.println(i > j);  // false 
		System.out.println(i < j);  // false
		System.out.println(i >= j); // true
		System.out.println(i <= j); // true
		System.out.println(i == j); // true
		System.out.println(i != j); // false
		
	}
}
